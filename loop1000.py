for x in range(1,1001,1) :       # loop starts from number 1, stops at 1001, increment of 1 step
    print (x)                    # Print x (number)


for y in range(2,1002,2) :       # loop starts from number 2, stops at 1002, increment of 2 steps
    print (y)                    # print y (number)  prints even number because of 2 steps at a time

for z in range(1000,0,-50) :     # loop from number 1000, stops at 0 in decrement of 50 steps
    print (z)                    # Print z (number)